package test.com;

public class Test02Switch {

	public static void main(String[] args) {
		int tmp = 99;
		switch(tmp) {
		case 33 : 
			System.out.println("hi 3");
			break;
		case 66 :
			System.out.println("hihi 6");			
			break;
		case 99 :
			System.out.println("hihihi 9");			
			break;
		default :
			System.out.println("no hi");
			break;
		}
		
	}
	
}
